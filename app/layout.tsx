import "./globals.css";
import { getCurrentUserOrNull } from "@/lib/auth/server";
import { UserProvider } from "@/components/UserContext";
import { redirect } from "next/navigation";
import { getSignInURL } from "@/lib/auth/client";
import { Inter } from "next/font/google";
import Image from "next/image";
import Logo from "@/app/_assets/logo.png";
import Link from "next/link";
import Breadcrumbs from "@/components/Breadcrumbs";

const inter = Inter({
  subsets: ["latin"],
  display: "swap",
});

export const metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  // TODO: This should get caught by global-error, but for some reason isn't
  const user = await getCurrentUserOrNull();
  if (!user) {
    redirect(getSignInURL(process.env.NEXT_PUBLIC_URL!));
  }
  return (
    <html lang="en">
      <body className={inter.className}>
        <UserProvider user={user}>
          <nav className="mb-4 flex h-[4.5rem] flex-row flex-nowrap items-center bg-dark px-2 text-light shadow-black/5">
            <Link href="/" className="inline-block">
              <Image
                src={Logo}
                alt=""
                placeholder="blur"
                height={96}
                className="max-h-[4.5rem] w-auto py-2"
              />
            </Link>
            <div className="ml-auto space-x-1">
              <span>Hello {user.first_name}</span>
            </div>
          </nav>
          <Breadcrumbs />
          <main className="mx-2 max-w-3xl lg:mx-auto">{children}</main>
        </UserProvider>
      </body>
    </html>
  );
}
